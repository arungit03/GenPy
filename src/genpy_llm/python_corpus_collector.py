        revision=_optional_string(item.get("revision"), f"{source_id}.revision"),
        include=_string_tuple(item.get("include", ["**/*.py"]), f"{source_id}.include"),
        exclude=_string_tuple(item.get("exclude", []), f"{source_id}.exclude"),
        discovered_automatically=bool(item.get("discovered_automatically", False)),
        repository_url=_optional_string(
            item.get("repository_url"), f"{source_id}.repository_url"
        ),
        stars=_optional_non_negative_int(item.get("stars"), f"{source_id}.stars"),
        repository_created_at=_optional_string(
            item.get("repository_created_at"), f"{source_id}.repository_created_at"
        ),
        repository_updated_at=_optional_string(
            item.get("repository_updated_at"), f"{source_id}.repository_updated_at"
        ),
        repository_pushed_at=_optional_string(
            item.get("repository_pushed_at"), f"{source_id}.repository_pushed_at"
        ),
        default_branch=_optional_string(
            item.get("default_branch"), f"{source_id}.default_branch"
        ),
        package_name=_optional_string(item.get("package_name"), f"{source_id}.package_name"),
        package_version=_optional_string(
            item.get("package_version"), f"{source_id}.package_version"
        ),
        release_date=_optional_string(item.get("release_date"), f"{source_id}.release_date"),
