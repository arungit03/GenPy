def _discover_automatic_import_sources(
    project_root: Path,
    imports_directory: Path,
    settings: Mapping[str, Any],
    configured_sources: tuple[CorpusSource, ...],
) -> tuple[CorpusSource, ...]:
    """Register first-level approved repositories and ZIPs from an import directory."""

    approval = _required_string(
        settings.get("approval"),
        "corpus_collection.automatic_imports.approval",
    )
    default_license = _optional_string(
        settings.get("default_license"),
        "corpus_collection.automatic_imports.default_license",
    )
    metadata_file = _required_string(
        settings.get("metadata_file", DEFAULT_IMPORT_METADATA_FILE),
        "corpus_collection.automatic_imports.metadata_file",
    )
    if Path(metadata_file).name != metadata_file:
        raise CorpusCollectionError(
            "corpus_collection.automatic_imports.metadata_file must be a filename."
        )
    default_include = _string_tuple(
        settings.get("include", ["**/*.py"]),
        "corpus_collection.automatic_imports.include",
    )
    default_exclude = _string_tuple(
        settings.get("exclude", []),
        "corpus_collection.automatic_imports.exclude",
    )
    discover_zip_archives = bool(settings.get("discover_zip_archives", True))
    if not imports_directory.exists():
        return ()
    if not imports_directory.is_dir():
        raise CorpusCollectionError(
            f"Automatic imports path is not a directory: {imports_directory}"
        )

    configured_locations = {
        _resolve(project_root, source.location)
        for source in configured_sources
        if source.source_type in {"local", "git", "zip"}
    }
    used_ids = {source.source_id for source in configured_sources}
    discovered: list[CorpusSource] = []
    for child in sorted(imports_directory.iterdir(), key=lambda path: path.name.casefold()):
        if child.is_symlink() or child.name.startswith("."):
            continue
        if child.is_dir():
            source_type = "git" if (child / ".git").exists() else "local"
            metadata_path = child / metadata_file
        elif discover_zip_archives and child.is_file() and child.suffix.casefold() == ".zip":
            source_type = "zip"
            metadata_path = child.with_name(f"{child.name}{metadata_file}")
        else:
            continue
        if child.resolve() in configured_locations:
            continue
        metadata = _load_import_metadata(metadata_path)
        source_id = _automatic_source_id(
            metadata.get("id", child.stem if source_type == "zip" else child.name),
            used_ids,
        )
        location = _portable_location(project_root, child)
        source = _load_source(
            {
                "id": source_id,
                "type": source_type,
                "location": location,
                "license": metadata.get("license", default_license),
                "approval": metadata.get("approval", approval),
                "revision": metadata.get("revision"),
                "include": metadata.get("include", default_include),
                "exclude": metadata.get("exclude", default_exclude),
                "discovered_automatically": True,
            }
        )
        used_ids.add(source.source_id)
        discovered.append(source)
    return tuple(discovered)


def _load_import_metadata(path: Path) -> dict[str, Any]:
    if not path.is_file():
        return {}
    try:
        value = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    except (OSError, UnicodeDecodeError, yaml.YAMLError) as exc:
        raise CorpusCollectionError(f"Invalid automatic import metadata {path}: {exc}") from exc
    if not isinstance(value, dict):
        raise CorpusCollectionError(
            f"Automatic import metadata must be a YAML mapping: {path}"
        )
    allowed = {"id", "license", "approval", "revision", "include", "exclude"}
    unknown = sorted(set(value) - allowed)
    if unknown:
        raise CorpusCollectionError(
            f"Unknown automatic import metadata fields in {path}: {', '.join(unknown)}"
        )
    return value


def _automatic_source_id(value: object, used_ids: set[str]) -> str:
    name = _required_string(value, "automatic import source id")
    slug = re.sub(r"[^A-Za-z0-9_.-]+", "-", name).strip(".-_").lower()
    if not slug:
        slug = hashlib.sha256(name.encode("utf-8")).hexdigest()[:12]
    base = f"import-{slug}"
    candidate = base
    if candidate in used_ids:
        suffix = hashlib.sha256(name.encode("utf-8")).hexdigest()[:8]
        candidate = f"{base}-{suffix}"
    counter = 2
    while candidate in used_ids:
        candidate = f"{base}-{counter}"
        counter += 1
    return candidate


def _portable_location(project_root: Path, path: Path) -> str:
    try:
        return path.resolve().relative_to(project_root.resolve()).as_posix()
    except ValueError:
        return str(path.resolve())


def _validate_config(config: CorpusCollectorConfig) -> None:
    if config.minimum_file_bytes < 0:
        raise CorpusCollectionError("corpus_collection.minimum_file_bytes must be non-negative.")
    if config.maximum_file_bytes <= config.minimum_file_bytes:
        raise CorpusCollectionError(
            "corpus_collection.maximum_file_bytes must exceed minimum_file_bytes."
        )
    if config.git_timeout_seconds <= 0:
        raise CorpusCollectionError("corpus_collection.git_timeout_seconds must be positive.")
    if not config.allowed_extensions:
        raise CorpusCollectionError("corpus_collection.allowed_extensions must not be empty.")
    for metadata_path in (config.provenance_manifest, config.report_path):
        try:
            metadata_path.resolve().relative_to(config.output_directory.resolve())
        except ValueError as exc:
            raise CorpusCollectionError(
                f"Collector metadata must be stored under {config.output_directory}: "
                f"{metadata_path}"
            ) from exc


def _source_path_selected(source: CorpusSource, path: PurePosixPath) -> bool:
    value = path.as_posix()
    return _matches_any(value, source.include) and not _matches_any(value, source.exclude)


def _matches_any(path: str, patterns: Iterable[str]) -> bool:
    return any(
        fnmatch.fnmatch(path, pattern)
        or PurePosixPath(path).match(pattern)
        or (pattern.startswith("**/") and fnmatch.fnmatch(path, pattern[3:]))
        for pattern in patterns
    )


def _is_supported_extension(path: str | PurePosixPath, extensions: Iterable[str]) -> bool:
    suffix = PurePosixPath(str(path)).suffix.casefold()
    return suffix in {extension.casefold() for extension in extensions}


def _is_python_path(path: str | PurePosixPath) -> bool:
    return PurePosixPath(str(path)).suffix.casefold() == ".py"


def _content_type(path: str | PurePosixPath) -> str:
    return "python_code" if _is_python_path(path) else "technical_text"


def _content_language(path: str | PurePosixPath) -> str:
    suffix = PurePosixPath(str(path)).suffix.casefold()
    if suffix == ".py":
        return "Python"
    if suffix == ".md":
        return "Markdown"
    if suffix == ".rst":
        return "reStructuredText"
    return "Text"


def _is_generated(config: CorpusCollectorConfig, path: PurePosixPath) -> bool:
    value = path.as_posix().casefold()
    name = path.name.casefold()
    return any(
        fnmatch.fnmatch(name, pattern.casefold())
        or fnmatch.fnmatch(value, pattern.casefold())
        for pattern in config.generated_file_patterns
    )


def _has_ignored_part(config: CorpusCollectorConfig, path: PurePosixPath) -> bool:
    ignored = tuple(item.casefold() for item in config.ignored_directories)
    return any(
        fnmatch.fnmatch(part.casefold(), pattern)
        for part in path.parts[:-1]
        for pattern in ignored
    )


def _unsafe_archive_path(path: PurePosixPath) -> bool:
    return (
        path.is_absolute()
        or not path.parts
        or any(part in {"", ".", ".."} for part in path.parts)
    )


def _safe_destination(root: Path, relative: PurePosixPath) -> Path:
    if _unsafe_archive_path(relative):
        raise CorpusCollectionError(f"Unsafe corpus destination path: {relative}")
    destination = root.joinpath(*relative.parts).resolve()
    try:
        destination.relative_to(root.resolve())
    except ValueError as exc:
        raise CorpusCollectionError(
            f"Corpus destination escapes output directory: {relative}"
        ) from exc
    return destination


def _is_within(path: Path, directory: Path) -> bool:
    try:
        path.resolve().relative_to(directory.resolve())
    except ValueError:
        return False
    return True


def _reject(statistics: SourceStatistics, reason: str) -> None:
    statistics.files_rejected += 1
    statistics.rejection_reasons[reason] += 1


def _resolve(project_root: Path, value: object) -> Path:
    text = _required_string(value, "path")
    candidate = Path(text).expanduser()
    return candidate.resolve() if candidate.is_absolute() else (project_root / candidate).resolve()


def _mapping(value: object, label: str) -> dict[str, Any]:
    if not isinstance(value, dict):
        raise CorpusCollectionError(f"{label} must be a mapping.")
    return value


def _required_string(value: object, label: str) -> str:
    if not isinstance(value, str) or not value.strip():
        raise CorpusCollectionError(f"{label} must be a non-empty string.")
    return value.strip()


def _optional_string(value: object, label: str) -> str | None:
    if value is None:
        return None
    if not isinstance(value, str):
        raise CorpusCollectionError(f"{label} must be a string or null.")
    return value.strip() or None


def _optional_non_negative_int(value: object, label: str) -> int | None:
    if value is None:
        return None
    if not isinstance(value, int) or isinstance(value, bool) or value < 0:
        raise CorpusCollectionError(f"{label} must be a non-negative integer or null.")
    return value


def _string_tuple(value: object, label: str) -> tuple[str, ...]:
    if not isinstance(value, (list, tuple)) or not all(
        isinstance(item, str) and item.strip() for item in value
    ):
        raise CorpusCollectionError(f"{label} must be a list of non-empty strings.")
    return tuple(item.strip() for item in value)


def _extension_tuple(value: object, label: str) -> tuple[str, ...]:
    extensions = _string_tuple(value, label)
    normalized: list[str] = []
    for extension in extensions:
        item = extension.casefold()
        if not item.startswith(".") or len(item) < 2:
            raise CorpusCollectionError(f"{label} entries must look like '.py'.")
        normalized.append(item)
    return tuple(dict.fromkeys(normalized))


def _bytes_hash(content: bytes) -> str:
    return hashlib.sha256(content).hexdigest()


def _file_hash(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as file:
        for chunk in iter(lambda: file.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def _timestamp() -> str:
    return datetime.now(UTC).isoformat()


