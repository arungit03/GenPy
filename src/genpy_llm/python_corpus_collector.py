def _load_current_manifest(
    config: CorpusCollectorConfig,
    sources: Sequence[CorpusSource] | None = None,
) -> dict[str, dict[str, Any]]:
    if not config.provenance_manifest.is_file():
        return {}
    manifest: dict[str, dict[str, Any]] = {}
    active_source_ids = {source.source_id for source in (sources or config.sources)}
    with config.provenance_manifest.open("r", encoding="utf-8") as file:
        for line_number, line in enumerate(file, start=1):
            if not line.strip():
                continue
            try:
                record = json.loads(line)
            except json.JSONDecodeError as exc:
                raise CorpusCollectionError(
                    f"Invalid provenance JSON in {config.provenance_manifest}:{line_number}: "
                    f"{exc}"
                ) from exc
            if not isinstance(record, dict) or not isinstance(record.get("stored_path"), str):
                raise CorpusCollectionError(
                    f"Invalid provenance record in {config.provenance_manifest}:{line_number}."
                )
            stored_path = str(record["stored_path"])
            try:
                target = _safe_destination(
                    config.output_directory,
                    PurePosixPath(stored_path),
                )
            except CorpusCollectionError:
                continue
            if not target.is_file():
                continue
            metadata = target.stat()
            digest = record.get("content_sha256")
            if (
                record.get("size_bytes") != metadata.st_size
                or record.get("mtime_ns") != metadata.st_mtime_ns
            ):
                actual_digest = _file_hash(target)
                if actual_digest != digest:
                    source = record.get("source")
                    if isinstance(source, dict) and source.get("id") in active_source_ids:
                        record["_content_stale"] = True
                        manifest[stored_path] = record
                    continue
                record["size_bytes"] = metadata.st_size
                record["mtime_ns"] = metadata.st_mtime_ns
            if isinstance(digest, str):
                manifest[stored_path] = record
    return manifest


def _manifest_hash_index(
    manifest: Mapping[str, Mapping[str, Any]],
) -> dict[str, set[str]]:
    result: dict[str, set[str]] = {}
    for stored_path, record in manifest.items():
        if record.get("_content_stale") is True:
            continue
        digest = record.get("content_sha256")
        if isinstance(digest, str):
            result.setdefault(digest, set()).add(stored_path)
    return result


def _collection_timestamp(record: Mapping[str, Any] | None) -> str:
    if record is not None:
        value = record.get("collection_timestamp")
        if isinstance(value, str) and value:
            return value
    return _timestamp()


def _write_manifest(path: Path, records: Iterable[Mapping[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    partial = Path(f"{path}.partial")
    partial.unlink(missing_ok=True)
    try:
        with partial.open("w", encoding="utf-8", newline="\n") as file:
            for record in sorted(records, key=lambda item: str(item["stored_path"])):
                json.dump(record, file, ensure_ascii=False, sort_keys=True, separators=(",", ":"))
                file.write("\n")
        os.replace(partial, path)
    except Exception:
        partial.unlink(missing_ok=True)
        raise


def _atomic_write_bytes(content: bytes, path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    partial = Path(f"{path}.partial")
    partial.unlink(missing_ok=True)
    try:
        partial.write_bytes(content)
        os.replace(partial, path)
    except Exception:
        partial.unlink(missing_ok=True)
        raise


def _atomic_json_dump(payload: Mapping[str, Any], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    partial = Path(f"{path}.partial")
    partial.unlink(missing_ok=True)
    try:
        with partial.open("w", encoding="utf-8", newline="\n") as file:
            json.dump(payload, file, ensure_ascii=False, indent=2, sort_keys=True)
            file.write("\n")
        os.replace(partial, path)
    except Exception:
        partial.unlink(missing_ok=True)
        raise


