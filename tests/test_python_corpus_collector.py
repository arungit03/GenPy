@pytest.mark.skipif(shutil.which("git") is None, reason="git is not installed")
def test_automatic_imports_discovers_local_and_git_repositories(tmp_path: Path) -> None:
    imports = tmp_path / "imports"
    local_repository = imports / "Plain Repository"
    (local_repository / "package").mkdir(parents=True)
    (local_repository / "package" / "local_module.py").write_text(
        "def local_value():\n    return 1\n",
        encoding="utf-8",
    )
    (local_repository / ".genpy-corpus.yaml").write_text(
        "license: MIT\napproval: Reviewed local fixture\n",
        encoding="utf-8",
    )

    git_repository = imports / "git-repository"
    git_repository.mkdir()
    (git_repository / "git_module.py").write_text(
        "class GitValue:\n    pass\n",
        encoding="utf-8",
    )
    (git_repository / ".genpy-corpus.yaml").write_text(
        "license: Apache-2.0\n",
        encoding="utf-8",
    )
    _git(git_repository, "init", "--quiet")
    _git(git_repository, "config", "user.email", "imports@example.invalid")
    _git(git_repository, "config", "user.name", "Imports Test")
    _git(git_repository, "add", ".")
    _git(git_repository, "commit", "--quiet", "-m", "fixture")
    revision = _git(git_repository, "rev-parse", "HEAD").stdout.strip()

    config = _config(tmp_path, [], automatic_imports=True)

    assert {source.source_id for source in config.sources} == {
        "import-git-repository",
        "import-plain-repository",
    }
    assert all(source.discovered_automatically for source in config.sources)

    result = collect_python_corpus(config)

    assert result.files_scanned == 2
    assert result.files_accepted == 2
    provenance = {
        record["source"]["id"]: record
        for record in _manifest(config.provenance_manifest)
    }
    assert provenance["import-plain-repository"]["license"] == "MIT"
    assert provenance["import-plain-repository"]["source"]["approval"] == (
        "Reviewed local fixture"
    )
    assert provenance["import-plain-repository"]["source"][
        "discovered_automatically"
    ] is True
    assert provenance["import-git-repository"]["license"] == "Apache-2.0"
    assert provenance["import-git-repository"]["source"]["type"] == "git"
    assert provenance["import-git-repository"]["source"]["revision"] == revision
    assert (
        tmp_path
        / "raw"
        / "import-plain-repository"
        / "package"
        / "local_module.py"
    ).is_file()

    report = json.loads(config.report_path.read_text(encoding="utf-8"))
    assert report["automatic_imports"]["discovered_source_count"] == 2
    assert report["automatic_imports"]["discovered_sources"] == [
        "import-git-repository",
        "import-plain-repository",
    ]

    second = collect_python_corpus(load_corpus_collector_config(config.config_path))
    assert second.files_accepted == 0
    assert second.files_unchanged == 2


def _config(
    root: Path,
    sources: list[dict[str, object]],
    *,
    minimum: int = 1,
    maximum: int = 10_000,
    automatic_imports: bool = False,
):
    config_path = root / "dataset_pipeline.yaml"
    payload = {
        "version": 1,
        "project_root": ".",
        "corpus_collection": {
            "output_directory": "raw",
            "provenance_manifest": "raw/collection_manifest.jsonl",
            "report": "raw/collection_report.json",
            "log_file": "logs/corpus.log",
            "minimum_file_bytes": minimum,
            "maximum_file_bytes": maximum,
            "git_timeout_seconds": 30,
            "sources": sources,
        },
        "logging": {"level": "INFO"},
    }
    if automatic_imports:
        payload["corpus_collection"]["automatic_imports"] = {
            "enabled": True,
            "directory": "imports",
            "approval": "Approved by test import-directory placement",
            "default_license": None,
        }
    config_path.write_text(yaml.safe_dump(payload, sort_keys=False), encoding="utf-8")
    return load_corpus_collector_config(config_path)


def _manifest(path: Path) -> list[dict[str, object]]:
    return [json.loads(line) for line in path.read_text(encoding="utf-8").splitlines() if line]


def _git(repository: Path, *arguments: str) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        ["git", "-C", str(repository), *arguments],
        check=True,
        capture_output=True,
        text=True,
    )
